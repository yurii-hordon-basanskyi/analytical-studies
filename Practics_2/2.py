# На вхід програми подається один рядок з цілими числами. Числа розділені пропусками.
# Необхідно вивести суму цих чисел. Наприклад, якщо був введений рядок чисел 2 -1 9 6, то
# результатом роботи програми буде їх сума 16.

input_string = input("Введіть числа розділені пропусками: ")

numbers = list(map(int, input_string.split()))
total_sum = sum(numbers)
print(total_sum)